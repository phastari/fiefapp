export const generateName = (noble: boolean = false): string => {
  if (noble) {
    if (Math.random() * 100 > 89) {
      return `${firstNames[Math.floor(Math.random() * firstNames.length)]} ${
        familyHigh[Math.floor(Math.random() * familyHigh.length)]
      }`;
    } else {
      return `${firstNames[Math.floor(Math.random() * firstNames.length)]} ${
        familyLow[Math.floor(Math.random() * familyLow.length)]
      }`;
    }
  }

  return `${firstNames[Math.floor(Math.random() * firstNames.length)]}`;
};

